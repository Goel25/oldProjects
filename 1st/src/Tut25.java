
public class Tut25 {
	public static void main(String[] args){
		System.out.println(Math.abs(-26)); //Absolute value
		System.out.println(Math.ceil(7.4));//Ceiling, rounds up
		System.out.println(Math.floor(7.9));//Floor, rounds down
		System.out.println(Math.max(5.1, 5.2));//Maximum, returns the higher value
		System.out.println(Math.min(5.1, 5.2));//Minimum, returns the lower value
		System.out.println(Math.pow(5, 3));//Power, takes first number to the power of the second
		System.out.println(Math.sqrt(25));//Square root
	}
}
